// ===================== 20. Média e situação =====================
let m1 = Number(prompt("20) Número 1:"));
let m2 = Number(prompt("Número 2:"));
let m3 = Number(prompt("Número 3:"));
let mediaFinal = (m1 + m2 + m3) / 3;

if (mediaFinal >= 7) alert("Aprovado! Média: " + mediaFinal);
else alert("Reprovado! Média: " + mediaFinal);