//Exercicío 12 - Média de 3 notas

let nota1 = parseInt(prompt("Digite o número da primeira nota"))
let nota2 = parseInt(prompt("Digite o número da segunda nota"))
let nota3 = parseInt(prompt("Digite o número da terceira nota"))

resultado = (nota1 + nota2 + nota3) /3

alert("O valor da média de suas 3 notas é: " +resultado )

